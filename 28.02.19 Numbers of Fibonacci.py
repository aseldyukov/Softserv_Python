# Вивести числа Фібоначі включно до введеного числа n, використовуючи цикли. 
# (Послідовність чисел Фібоначі 0, 1, 1, 2, 3, 5, 8, 13 і т.д.)
def fibo(n):
    fib = [0, 1]
    i = 0

    while i <= n-2:
        i += 1                    
        fib.append(0)
        fib[-1] = fib[-2] + fib[-3]
        
    return fib
print(fibo(6))
